// This Program will Tell us about Increment & Decrement Operator
// Prefix & Postfix Operators.

#include<iostream>
using namespace std;

int main(){
	int x,y; // Let 2 Variables.
	y = 40;
	x = y++; // X is equal to y
	         // This is Prefix operator
	         
	/* This program will show how to use Pre & Post Operator
	and display the Output at the end of Execution of Program*/
	
	/* If we use ( ++y ) Prefix Operator ,the result will remain same.
	But if we use Postfix Operator ( y++ ) ,the result will different from each other */  
	
	
	
	cout << x << endl;
	cout << y << endl;
	return 0;
	
}
