/* This Program is about Do-While Loop.
   This is A simple Loop which is Display A message
   10 times on the Screen. */
   
#include<iostream>
using namespace std;
int main()
{
	int i=0;
	do
	{
		cout <<"Hello"<<endl;
		i++;        // i++ is same in While Loop
	} while(i<=10); // while Condition
	cout <<"Bye";
}
