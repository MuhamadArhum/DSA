/* In This Program, We will Leran About while loop.
We alse Leran how while loop syntax are being write and 
Compile.*/

#include<iostream>
using namespace std;
int main()
{
	int i = 1;
	while(i<=10)    // While Loop & Its Condition
	{
		cout<<i<<" ARHUM"<<endl;
		i++; // Increment Operator
		
		/* Increment & Decrement Operator is always write in 
		Curly Bracket of while loop */
	}
	cout<<"Bye"<<endl;
}
